package com.cs.bru.model;

public class Subject {
	private int SubjectID;
	private String ThaiName;
	private String LearnUnit;
	private String SubjectStatus;
	private int Patibad;
	private int Tudsadee;
	public int getSubjectID() {
		return SubjectID;
	}
	public void setSubjectID(int subjectID) {
		SubjectID = subjectID;
	}
	public String getThaiName() {
		return ThaiName;
	}
	public void setThaiName(String thaiName) {
		ThaiName = thaiName;
	}
	public String getLearnUnit() {
		return LearnUnit;
	}
	public void setLearnUnit(String learnUnit) {
		LearnUnit = learnUnit;
	}
	public String getSubjectStatus() {
		return SubjectStatus;
	}
	public void setSubjectStatus(String subjectStatus) {
		SubjectStatus = subjectStatus;
	}
	public int getPatibad() {
		return Patibad;
	}
	public void setPatibad(int patibad) {
		Patibad = patibad;
	}
	public int getTudsadee() {
		return Tudsadee;
	}
	public void setTudsadee(int tudsadee) {
		Tudsadee = tudsadee;
	}
	
}

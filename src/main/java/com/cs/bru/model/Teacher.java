package com.cs.bru.model;

public class Teacher {
	private int ThID;
	private String TeachID;
	private String TeacherPass;
	private String TeacherName;
	private String TeacherFname;
	public int getThID() {
		return ThID;
	}
	public void setThID(int thID) {
		ThID = thID;
	}
	public String getTeachID() {
		return TeachID;
	}
	public void setTeachID(String teachID) {
		TeachID = teachID;
	}
	public String getTeacherPass() {
		return TeacherPass;
	}
	public void setTeacherPass(String teacherPass) {
		TeacherPass = teacherPass;
	}
	public String getTeacherName() {
		return TeacherName;
	}
	public void setTeacherName(String teacherName) {
		TeacherName = teacherName;
	}
	public String getTeacherFname() {
		return TeacherFname;
	}
	public void setTeacherFname(String teacherFname) {
		TeacherFname = teacherFname;
	}
	
	
	
	
	
}
